interface NotebookFixture {
  name: string;
  path: string;
  cellCount: number;
  bytes: number;
  external: boolean;
}

interface LoadBenchEvent {
  name: string;
  t: number;
  detail?: {
    count?: number;
  };
}

interface LoadBenchResult {
  fixture: NotebookFixture;
  events: LoadBenchEvent[];
  timings: {
    toolbarVisibleMs: number | null;
    firstCellAttachedMs: number | null;
    allCellsAttachedMs: number | null;
    notebookSyncedMs: number | null;
    sessionReadyMs: number | null;
  };
  cellTimeline: Array<{ t: number; count: number | null }>;
}

