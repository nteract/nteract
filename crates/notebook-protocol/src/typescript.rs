/// One generated capability predicate: TS function name, doc phrase, and the
/// `ConnectionScope` method it mirrors.
type ScopeCapabilityRow = (&'static str, &'static str, fn(ConnectionScope) -> bool);

/// Capability predicates exported to TypeScript for hosted room connections.
/// Local daemon connections share the same scope model, but use a local-only
/// blob-upload predicate so same-UID editor attachments keep working until
/// hosted editor uploads ship with reference-path validation.
const SCOPE_CAPABILITIES: &[ScopeCapabilityRow] = &[
    (
        "allowsNotebookWrite",
        "send NotebookDoc changes",
        ConnectionScope::allows_notebook_write,
    ),
    (
        "allowsRuntimeStateWrite",
        "send RuntimeStateDoc changes",
        ConnectionScope::allows_runtime_state_write,
    ),
    (
        "allowsBlobUpload",
        "upload blobs on hosted room connections (PUT_BLOB frames and multipart upload requests)",
        ConnectionScope::allows_blob_upload,
    ),
    (
        "allowsExecutionRequestSubmit",
        "submit execution requests (owner-only until an explicit execute capability exists)",
        ConnectionScope::allows_execution_request,
    ),
    (
        "allowsPublish",
        "publish revisions",
        ConnectionScope::allows_publish,
    ),
    (
        "allowsAclMutation",
        "manage ACLs",
        ConnectionScope::allows_acl_mutation,
    ),
];

fn generated_scope_capability_fns() -> String {
    SCOPE_CAPABILITIES
        .iter()
        .map(|(name, doc, predicate)| {
            let allowed = ConnectionScope::ALL
                .iter()
                .copied()
                .filter(|scope| predicate(*scope))
                .map(|scope| format!("scope === \"{}\"", scope.as_str()))
                .collect::<Vec<_>>()
                .join(" || ");
            let body = if allowed.is_empty() {
                "  return false;".to_string()
            } else {
                format!("  return {allowed};")
            };
            format!(
                "/** Whether this scope can {doc}. */\nexport function {name}(scope: ConnectionScope): boolean {{\n{body}\n}}"
            )
        })
        .collect::<Vec<_>>()
        .join("\n\n")
}

pub fn generated_scope_capabilities_ts() -> String {
    let scope_union = ConnectionScope::ALL
        .iter()
        .map(|scope| format!("\"{}\"", scope.as_str()))
        .collect::<Vec<_>>()
        .join(" | ");
    let scope_list = ConnectionScope::ALL
        .iter()
        .map(|scope| format!("  \"{}\",", scope.as_str()))
        .collect::<Vec<_>>()
        .join("\n");
    format!(
        r#"{GENERATED_HEADER}/**
 * Connection-scope capability lattice generated from
 * `nteract_identity::ConnectionScope`.
 *
 * This is the single source of truth for what each scope may do on a
 * notebook room connection. The hosted room (Cloudflare Worker) and any
 * TypeScript client must use these predicates instead of hand-rolled
 * scope checks so they cannot drift from the daemon's Rust enforcement.
 */
export type ConnectionScope = {scope_union};

/** Every scope, in privilege order. */
export const CONNECTION_SCOPES = [
{scope_list}
] as const satisfies readonly ConnectionScope[];

export function isConnectionScope(value: string | null | undefined): value is ConnectionScope {{
  return (CONNECTION_SCOPES as readonly string[]).includes(value ?? "");
}}

export function parseConnectionScope(value: string): ConnectionScope {{
  if (isConnectionScope(value)) {{
    return value;
  }}
  throw new Error(`unknown connection scope: ${{value}}`);
}}

{capability_fns}
"#,
        capability_fns = generated_scope_capability_fns(),
    )
}

