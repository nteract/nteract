fn workspace_root() -> std::io::Result<PathBuf> {
    Path::new(env!("CARGO_MANIFEST_DIR"))
        .ancestors()
        .nth(2)
        .map(Path::to_path_buf)
        .ok_or_else(|| {
            std::io::Error::new(
                std::io::ErrorKind::NotFound,
                "notebook-protocol manifest is not under a workspace crates directory",
            )
        })
}

pub fn write_generated_files() -> std::io::Result<()> {
    let root = workspace_root()?;
    std::fs::write(
        root.join("packages/runtimed/src/wire-constants.ts"),
        generated_wire_constants_ts(),
    )?;
    std::fs::write(
        root.join("packages/runtimed/src/request-types.ts"),
        generated_request_types_ts(),
    )?;
    std::fs::write(
        root.join("packages/runtimed/src/protocol-contract.ts"),
        generated_protocol_contract_ts(),
    )?;
    std::fs::write(
        root.join("packages/runtimed/src/scope-capabilities.ts"),
        generated_scope_capabilities_ts(),
    )?;
    Ok(())
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn generated_typescript_bindings_are_current() -> std::io::Result<()> {
        let root = workspace_root()?;
        let request_types =
            std::fs::read_to_string(root.join("packages/runtimed/src/request-types.ts"))?;
        let protocol_contract =
            std::fs::read_to_string(root.join("packages/runtimed/src/protocol-contract.ts"))?;
        let wire_constants =
            std::fs::read_to_string(root.join("packages/runtimed/src/wire-constants.ts"))?;
        let scope_capabilities =
            std::fs::read_to_string(root.join("packages/runtimed/src/scope-capabilities.ts"))?;

        assert_eq!(
            wire_constants,
            generated_wire_constants_ts(),
            "packages/runtimed/src/wire-constants.ts is stale; run `cargo run -p notebook-protocol --bin generate-runtimed-types`"
        );

        assert_eq!(
            scope_capabilities,
            generated_scope_capabilities_ts(),
            "packages/runtimed/src/scope-capabilities.ts is stale; run `cargo run -p notebook-protocol --bin generate-runtimed-types`"
        );

        assert_eq!(
            request_types,
            generated_request_types_ts(),
            "packages/runtimed/src/request-types.ts is stale; run `cargo run -p notebook-protocol --bin generate-runtimed-types`"
        );
        assert_eq!(
            protocol_contract,
            generated_protocol_contract_ts(),
            "packages/runtimed/src/protocol-contract.ts is stale; run `cargo run -p notebook-protocol --bin generate-runtimed-types`"
        );
        Ok(())
    }
}
