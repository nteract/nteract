fn quoted_array(values: &[&str], indent: &str) -> String {
    values
        .iter()
        .map(|value| format!("{indent}\"{value}\","))
        .collect::<Vec<_>>()
        .join("\n")
}

fn generated_frame_type_object() -> String {
    FRAME_TYPES
        .iter()
        .map(|(name, value)| format!("  {name}: 0x{value:02x},"))
        .collect::<Vec<_>>()
        .join("\n")
}

fn generated_frame_size_limits_object() -> String {
    FRAME_TYPES
        .iter()
        .map(|(name, value)| {
            let limits = frame_size_limits(*value);
            format!(
                "  [FrameType.{name}]: {{ cap: {}, warn: {} }},",
                limits.cap, limits.warn
            )
        })
        .collect::<Vec<_>>()
        .join("\n")
}

